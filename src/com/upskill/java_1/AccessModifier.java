package com.upskill.java_1;

public class AccessModifier {
	
	// public - any class can access
	
	
	//private- only same class can access
	
	//protected - classes in the same package and subclass
	
	// default- classes in the same package can access
	
	public String name = "Shah"; // public - any class can access
	
	private int age = 25; //private- only same can access
	
	protected int ssn = 1234; //protected - classes in the same package and subclass
	
	String address = "Heritage Tower"; // default- classes in the same package can access
}
